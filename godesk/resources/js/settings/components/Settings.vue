<template>
  <loading-view :loading="loading" :key="$route.params.id">
    <form v-if="panels" @submit.prevent="update" autocomplete="off">
      <template v-for="panel in panelsWithFields">
        <template v-if="panel.component === 'detail-tabs' || panel.component === 'form-tabs'">
          <h1 class="text-90 font-normal text-2xl mb-3 nova-heading">{{ panel.name }}</h1>
          <form-tabs
              :resource-name="'settings'"
              :resource-id="'settings'"
              :errors="validationErrors"
              :field="{ component: 'tabs', fields: panel.fields }"
              :name="panel.name"
              class="mb-3"
          />
        </template>
        <form-panel
            v-else
            :panel="panel"
            :name="panel.name"
            :key="panel.name"
            :fields="panel.fields"
            :resource-name="'settings'"
            :resource-id="'settings'"
            mode="form"
            class="mb-6"
            :validation-errors="validationErrors"
        />
      </template>
      <div class="flex items-center">
        <progress-button type="submit" class="ml-auto" :disabled="isUpdating" :processing="isUpdating">
          {{ this.__('Save') }}
        </progress-button>
      </div>
    </form>

    <div class="py-3 px-6 border-50" v-else>
      <div class="flex">
        <div class="w-1/4 py-4">
          <h4 class="font-normal text-80">{{ this.__('Error') }}</h4>
        </div>
        <div class="w-3/4 py-4">
          <p class="text-90">{{ this.__('No settings fields have been defined.') }}</p>
        </div>
      </div>
    </div>
  </loading-view>
</template>

<script>
import {Errors} from 'laravel-nova'

export default {
  metaInfo() {
    return {
      title: this.__('Settings'),
    }
  },
  data() {
    return {
      loading: false,
      isUpdating: false,
      fields: [],
      panels: [],
      validationErrors: new Errors(),
    };
  },
  async created() {
    await this.getFields();
  },
  watch: {
    $route(to, from) {
      if (to.params.id !== from.params.id) this.getFields();
    },
  },
  methods: {
    async getFields() {
      this.loading = true;
      this.fields = [];
      const params = {editing: true, editMode: 'update'};
      if (this.$route.params.id) params.path = this.$route.params.id;
      const {
        data: {fields, panels},
      } = await Nova.request()
          .get('/nova-vendor/settings', {params})
          .catch(error => {
            if (error.response.status === 404) {
              this.$router.push({name: '404'});
              return;
            }
          });
      this.fields = fields;
      this.panels = panels;
      this.loading = false;
    },
    async update() {
      try {
        this.isUpdating = true;
        const response = await this.updateRequest();
        if (response && response.data && response.data.reload === true) {
          location.reload();
          return;
        }
        this.$toasted.show(this.__('Settings successfully updated!'), {
          type: 'success',
        });
        await this.getFields();
        this.isUpdating = false;
        this.validationErrors = new Errors();
      } catch (error) {
        console.error(error);
        this.isUpdating = false;
        if (error && error.response && error.response.status === 422) {
          this.validationErrors = new Errors(error.response.data.errors);
        }
      }
    },
    updateRequest() {
      return Nova.request().post('/nova-vendor/settings', this.formData);
    },
  },
  computed: {
    formData() {
      return _.tap(new FormData(), formData => {
        _(this.fields).each(field => field.fill(formData));
        formData.append('_method', 'POST');
        if (this.$route.params.id) formData.append('path', this.$route.params.id);
      });
    },
    panelsWithFields() {
      return _.map(this.panels, panel => {
        return {
          name: panel.name,
          component: panel.component,
          fields: _.filter(this.fields, field => field.panel === panel.name),
        };
      });
    },
  },
};
</script>

